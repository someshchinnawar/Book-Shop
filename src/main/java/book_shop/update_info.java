    private void btnsearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsearchActionPerformed
        // TODO add your handling code here:

        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://"+
                "localhost:3306/book_shop","root", "root");
            String query =  "select * from addbook where id = ?";
            PreparedStatement p = con.prepareStatement(query);
            p.setInt(1, Integer.parseInt(txtnewbookid.getText()));
            ResultSet rs = p.executeQuery();
            if(rs.next())
            {
                txtnewbookname.setText(rs.getString("bookname"));
                txtnewbookaname.setText(rs.getString("authorname"));
                txtnewbookprice.setText(rs.getString("bookprice"));
                txtnoofcopies.setText(rs.getString("no_of_copies"));
            }
            else{
                JOptionPane.showMessageDialog(null, "Record not found");
            }
            
        }
        catch(Exception e){
            System.out.println(e);
        }

    }//GEN-LAST:event_btnsearchActionPerformed

