    private void btnupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnupdateActionPerformed
        // TODO add your handling code here:
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://"+
                "localhost:3306/book_shop","root", "root");
            String query =  "update addbook set bookname = ? , authorname = ? , bookprice = ? , no_of_copies = ? where id = ?";
            PreparedStatement p = con.prepareStatement(query);
           
            p.setString(1, txtnewbookname.getText());
            p.setString(2, txtnewbookaname.getText());
            p.setInt(3, Integer.parseInt(txtnewbookprice.getText()));
            p.setInt(4, Integer.parseInt(txtnoofcopies.getText()));
            p.setInt(5, Integer.parseInt(txtnewbookid.getText()));
            
            p.executeUpdate();
            
        }   
        catch(Exception e){
            System.out.println(e);
        }
        JOptionPane.showMessageDialog(null, "Updated book info");
        new book().setVisible(true);
        dispose();
    }//GEN-LAST:event_btnupdateActionPerformed

